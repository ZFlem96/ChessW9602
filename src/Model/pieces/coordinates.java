package Model.pieces;

/**
 * current x and y coordinates of any piece
 */
public class coordinates {
    public int x;
    public int y;

    /**
     * class constructor
     * @param x : x coordinate
     * @param y : y coordinate
     */
    coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
